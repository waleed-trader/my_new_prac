def calculate_stats(marks_list):
    avg = sum(marks_list) / len(marks_list) if marks_list else 0
    high = max(marks_list) if marks_list else 0
    low = min(marks_list) if marks_list else 0
    return avg, high, low


students = data[0]["students"]

class_results = {}
for student in students:
    cls = student["Class"]
    total_marks = sum(student["Marks"].values())
    if cls not in class_results:
        class_results[cls] = []
    class_results[cls].append(total_marks)

print(" Class-wise Results:")
for cls, marks_list in class_results.items():
    avg, high, low = calculate_stats(marks_list)
    print(f"Class {cls}: Avg={avg:.2f}, High={high}, Low={low}")

all_marks = []
for marks_list in class_results.values():
    all_marks.extend(marks_list)

avg, high, low = calculate_stats(all_marks)
print("\n Overall School Results:")
print(f"All Classes Combined: Avg={avg:.2f}, High={high}, Low={low}")
